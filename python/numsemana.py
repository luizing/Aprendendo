num = int(input("Digite o número que deseja converter para dia da semana: "))

if num == 1:
    print("domingo")
elif num == 2:
    print("segunda")
elif num == 3:
    print("terça")
elif num == 4:
    print("quarta")
elif num == 5:
    print("quinta")
elif num == 6:
    print("sexta")
elif num == 7:
    print("sabado")
else:
    print("Digite um número entre 1 e 7 !")